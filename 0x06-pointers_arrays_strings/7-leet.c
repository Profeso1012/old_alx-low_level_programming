#include "main.h"

/**
 * leet - Function that encodes a string into 1337.
 * @str: Pointer to string to be encoded
 * Return: Pointer to the encoded string
 */

char *leet(char *str)
{
	int i, j;
	char c[] = "aAeEoOtTlL";
	char d[] = "4433007711";

	for (i = 0; str[i] != '\0'; i++)
	{
		for (j = 0; c[j] != '\0'; j++)
		{
			if (str[i] == c[j])
			{
				str[i] = d[j];
			}
		}
	}
	return (str);
}
